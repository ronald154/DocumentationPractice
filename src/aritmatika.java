public class aritmatika {
    static void main() {
        int a = 6;
        int b = 3;
        int tambah = a + b;
        int kurang = a - b;
        int bagi = a / b;

        System.out.println("Hasil dari tambah " + a + " + " + b + " adalah " + tambah);
        System.out.println("Hasil dari kurang " + a + " - " + b + " adalah " + kurang);
        System.out.println("Hasil dari bagi " + a + " / " + b + " adalah " + bagi);
    }
}
